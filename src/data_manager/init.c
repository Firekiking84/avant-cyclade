#include		"data_manager.h"

void			efinit_data_manager(t_data_manager	*manager)
{
  if (!manager)
    return;
  manager->stage = SD_DEST;
  manager->index_byte = 0;
  manager->index_bit = 0;
  manager->cur_byte = 0;
}
