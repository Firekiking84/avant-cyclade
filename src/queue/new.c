#include		"queue.h"

t_queue			*efnew_queue()
{
  t_queue		*new_queue;

  new_queue = bunny_malloc(sizeof(t_queue));
  if (!new_queue)
    return(NULL);
  new_queue->front = NULL;
  new_queue->back = NULL;
  new_queue->nb_node = 0;
  return(new_queue);
}
